    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (cmbEstado.getSelectedIndex() == 1 || cmbEstado.getSelectedIndex() == 2) {
            JOptionPane.showMessageDialog(this, "Não foi possível adicionar pois a sala não está disponível!", "Erro!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Sessao sessao = new Sessao(filme, sala, (Date) spnData.getValue());
            gerSessao.adicionar(sessao);
        } catch (Exception e) {
            System.out.println("Erro: " + e);
            JOptionPane.showMessageDialog(this, "Não foi possível adicionar! \nRevise os campos e tente novamente.", "Erro!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JOptionPane.showMessageDialog(this, "Cadastrado com sucesso!");
        limparCampos();
    }//GEN-LAST:event_btnConfirmarActionPerformed

