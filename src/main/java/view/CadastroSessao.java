    private void carregaImagem(String caminhoImagem) {
        BufferedImage imagem;
        File file = new File(caminhoImagem);
        if (!caminhoImagem.equals(imagemDefault)) {
            try {
                imagem = ImageIO.read(file);
                btnImagem.setIcon(new ImageIcon(imagem.getScaledInstance(180, 260, 100)));
            } catch (IOException ex) {
                System.out.println("Erro: " + ex);
                Logger.getLogger(ConsultaFilmes.class.getName()).log(Level.SEVERE, "Imagem não encontrada!", ex);
            }
        } else {
            try {
                imagem = ImageIO.read(file);
                btnImagem.setIcon(new ImageIcon(imagem));
            } catch (IOException ex) {
                System.out.println("Erro: " + ex);
                Logger.getLogger(ConsultaFilmes.class.getName()).log(Level.SEVERE, "Imagem não encontrada!", ex);
            }
        }
    }

    private void listarFilmes() {
        cmbFilme.removeAllItems();
        gerFilme.relatorio().forEach(el -> cmbFilme.addItem(el.getTitulo()));
    }

    private void listarSalas() {
        cmbSala.removeAllItems();
        gerSala.relatorio().forEach(el -> cmbSala.addItem(String.valueOf(el.getNumSala())));
    }

    private void limparCampos() {
        cmbEstado.setSelectedIndex(0);
        cmbFilme.setSelectedIndex(0);
        cmbSala.setSelectedItem(0);
        cmbSala.setSelectedIndex(0);
        cmbFilme.setSelectedIndex(0);
        
    }

